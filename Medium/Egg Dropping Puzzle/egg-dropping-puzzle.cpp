class Solution
{
    public:
    //Function to find minimum number of attempts needed in 
    //order to find the critical floor.
    int eggDrop(int n, int k) 
    {
        vector<int> dp(n+1, 0);
        int ans = 0;
        while(dp[n]<k){
            ans++;
            for(int egg=n; egg>0; egg--){
                dp[egg] += 1 + dp[egg-1];
            }
        }
        return ans;
    }
};

