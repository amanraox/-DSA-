class Solution{
  public:
    long long countSubArrayProductLessThanK(const vector<int>& a, int n, long long k) {
        long long curr = 1, res = 0, start = 0;
        for(int i = 0; i < n; i++) {
            curr = curr * a[i];
            while(start <= i && curr >= k) curr /= a[start++];
            res += (i - start + 1);
        }
        return res;
    }
};

