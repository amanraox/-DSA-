class Solution {
  public:
    int maximumProfit(vector<int> &prices) {int n=prices.size();
        int profit=0;
        int maxe=0;
        for(int i=n-1;i>=0;i--)
        {
            maxe=max(maxe,prices[i]);
            profit=max(profit,maxe-prices[i]);
        }
        return profit;
    }
};


