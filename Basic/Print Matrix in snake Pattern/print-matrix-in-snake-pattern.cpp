class Solution
{   
    public:
    //Function to return list of integers visited in snake pattern in matrix.
    vector<int> snakePattern(vector<vector<int> > matrix)
    {   
        // code here
        vector<int> ans;
        int n=matrix.size();
        for(int j=1;j<=n;j++)
        {
            if(j&1)
                {for(int i=0;i<n;i++)
                ans.push_back(matrix[j-1][i]);}
            else
            {for(int i=n-1;i>=0;i--)
                ans.push_back(matrix[j-1][i]);}
        }
        return ans;
    }
};

